class Product {
  final String name;
  final String id;
  final String productImage;
  final double price;
  final String brandName;
  final String category;
  final String description;

  Product({
    required this.name,
    required this.id,
    required this.productImage,
    required this.price,
    required this.brandName,
    required this.category,
    required this.description,
  });
}
